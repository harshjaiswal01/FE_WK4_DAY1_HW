// Task 1: Declare variables to represent student information such as name, age, grade, and attendance status.

// Declare a variable to store the student's name (string data type)
var studentName;

// Declare a variable to store the student's age (number data type)
var studentAge;

// Declare a variable to store the student's grade (string data type, can also be a number)
var studentGrade;

// Declare a variable to store the student's attendance status (boolean data type)
var studentAttendance;

// Task 2: Assign sample values to the student information variables.

// Assign a sample name to the studentName variable
studentName = "Val J";

// Assign a sample age to the studentAge variable
studentAge = 11;

// Assign a sample grade to the studentGrade variable
studentGrade = "A";

// Assign a sample attendance status to the studentAttendance variable
studentAttendance = true;

// Task 3: Display the student information using console.log() statements.

// Display the student's name
console.log("Student Name: " + studentName);

// Display the student's age
console.log("Student Age: " + studentAge);

// Display the student's grade
console.log("Student Grade: " + studentGrade);

// Display the student's attendance status
// Use a ternary operator to display "Present" if true, "Absent" if false
console.log("Student Attendance: " + (studentAttendance ? "Present" : "Absent"));
